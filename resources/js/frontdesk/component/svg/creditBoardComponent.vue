<script>
export default {
  props: ["amount", "color"],
  data: () => ({
    bigR: window.innerWidth > 767 ? 21.4 : 30,
    smallR: window.innerWidth > 767 ? 12.9 : 25,
    scx: window.innerWidth > 767 ? 362.111 : 350.517,
    scy: window.innerWidth > 767 ? 326.169 : 207.679,
    key: 0,
    amountTick: 0,
    amountCount: 5000,
    rate: 20,
    rateTick: 0,
    rateCount: 5,
    pmt: 0,
    tweenedPmt: 0,
    moveEl: "",
    pageY: "",
    target: "",
    periods: [3, 6, 12, 18, 24],
    big: {
      deg: 105,
      cx: 263.9,
      cy: 253.6,
      r: 208.5,
    },
    small: {
      deg: window.innerWidth > 767 ? 153 : 196,
      cx: 506.8,
      cy: 252.2,
      r: 162.5,
    },
  }),
  created() {
    this.calculation();
  },
  mounted() {
    if (this.color) {
      $("#ccrata .st5").css("fill", this.color);
    }
    this.$nextTick(() => {
      this.touchMove();
    });
  },
  watch: {
    pmt(newVal) {
      gsap.to(this.$data, { duration: 0.5, tweenedPmt: newVal });
    },
    key() {
      this.calculation();
    },
  },
  methods: {
    touchMove() {
      this.$refs.big.addEventListener("touchstart", this.handleTouchStart, false);
      this.$refs.small.addEventListener("touchstart", this.handleTouchStart, false);
      this.$refs.ccrata.addEventListener("touchstart", this.handleTouchStart, false);
      this.$refs.ccrata.addEventListener("touchmove", this.handleTouchMove, false);
      this.$refs.ccrata.addEventListener("touchend", this.handleTouchtouchend, false);
    },
    handleTouchStart(event) {
      $("body").css("overflow", "hidden");
      this.moveEl = event.target;
      this.target = $(event.target).attr("id");
      if (this.target === "big") {
        this.amountTick = (this.amount - 5000) / 75;
      }
      if (this.target === "small") {
        this.rateTick = Math.round(((this.rate - 5) / 76) * 100) / 100;
      }
    },
    handleTouchMove(event) {
      $("body").css("overflow", "hidden");
      this.moving(event.touches[0]);
    },
    handleTouchtouchend() {
      $("body").css("overflow", "initial");
      this.moveEl = "";
      this.target = "";
    },
    format(data) {
      data = parseInt(data);
      if (!isNaN(data)) {
        let l10nEN = new Intl.NumberFormat("en-US");
        return l10nEN.format(data.toFixed(0));
      }
      return 0;
    },
    plus() {
      if (this.key < 4) {
        this.key++;
      }
    },
    minus() {
      if (this.key > 0) {
        this.key--;
      }
    },
    getTarget(e, target) {
      this.moveEl = e.target;
      this.target = target;
      if (this.target === "big") {
        this.amountTick = (this.amount - 5000) / 75;
      }
      if (this.target === "small") {
        this.rateTick = Math.round(((this.rate - 5) / 76) * 100) / 100;
      }
    },
    removeTarget() {
      this.moveEl = "";
      this.target = "";
    },
    moving(e) {
      let pageY = e.pageY;
      if (this.pageY > pageY) {
        if (this.target === "big") {
          if (this.big.deg < 255) {
            this.big.deg += 2;
            this.amountCount += this.amountTick;
          }
        }

        let min = window.innerWidth > 767 ? 1 : 45;
        if (this.target === "small") {
          if (this.small.deg > min) {
            this.small.deg -= 2;
            this.rateCount += this.rateTick;
          }
        }
      } else {
        if (this.target === "big") {
          if (this.big.deg > 105) {
            this.big.deg -= 2;
            this.amountCount -= this.amountTick;
          }
        }
        let max = window.innerWidth > 767 ? 153 : 196;
        if (this.target === "small") {
          if (this.small.deg < max) {
            this.small.deg += 2;
            this.rateCount -= this.rateTick;
          }
        }
      }

      if (this.amountCount < 5000) this.amountCount = 5000;
      if (Math.floor(this.amountCount) === 119999) this.amountCount = 120000;

      console.log(this.small.deg);
      this.pageY = pageY;
      if (this.target) {
        this.slide(e);
        this.calculation();
      }
    },
    slide(e) {
      let pageY = e.pageY - $("#ccrata").offset().top;
      let pageX = e.pageX - $("#ccrata").offset().left;

      let cx =
        this[this.target]["cx"] +
        this[this.target]["r"] * Math.cos((this[this.target]["deg"] * 3.14) / 180);
      let cy =
        this[this.target]["cy"] +
        this[this.target]["r"] * Math.sin((this[this.target]["deg"] * 3.14) / 180);

      if (cx + 300 > pageX && cx - 300 < pageX && cy + 300 > pageY && cy - 300 < pageY) {
        $(this.moveEl).attr("cx", cx);
        $(this.moveEl).attr("cy", cy);
      }
    },
    calculation() {
      let m_rate = Math.round(this.rateCount) / 1200;
      let nper = Math.pow(m_rate + 1, -parseInt(this.periods[this.key]));
      this.pmt = Math.ceil((this.amountCount * m_rate) / (1 - nper));
    },
  },
};
</script>

